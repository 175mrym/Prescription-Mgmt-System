    private void jBtnDelete1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDelete1ActionPerformed
        // TODO add your handling code here:
        
        // Get the supplier ID from the text field and remove leading/trailing whitespace
    String supplierID = jTfID.getText().trim();
    
    // Check if the supplier ID is empty
    if (supplierID.isEmpty()) {
        // Display an error message if the ID is empty
        jLabelStatus.setText("Please enter the ID of the supplier to delete");
        return; // Exit the method if the ID is empty
    }
    
    // Create an instance of SupplierDAO to interact with the database
    SupplierDAO supplierDAO = new SupplierDAO();
    int deleteStatus = -1; // Initialize delete status
    
    try {
        // Check if the supplier with the given ID exists in the database
        if (supplierDAO.findSupplierById(supplierID) == null) {
            jLabelStatus.setText("Supplier ID does not exist in the DB");
            return; // Exit the method if the supplier doesn't exist
        }
        
        // Delete the supplier from the database
        deleteStatus = supplierDAO.deleteSupplier(supplierID);
    } catch (SQLException e) {
        // Handle any SQL exceptions by displaying the error message
        jLabelStatus.setText("Database Error: " + e.getMessage());
    }
    
     // Check if the delete operation was successful
    if (deleteStatus == 1) {
        // Display success message if the supplier is deleted
        jLabelStatus.setText("Supplier " + supplierID + " has been successfully deleted!");
        // Clear the text fields
        jTfID.setText("");
        jTfName.setText("");
        jTfInfo.setText("");
        // Reload all suppliers to reflect the changes
        loadAllSupplier();
    } else {
        // Display error message if there was an error while deleting the supplier
        jLabelStatus.setText("Failed to delete Supplier " + supplierID);
    }
    
    }//GEN-LAST:event_jBtnDelete1ActionPerformed

