      private final void loadAllSupplier() {
    // Step 1: Set the column names of the JTable
        Vector colNameV = new Vector();
        colNameV.add("Supplier ID");
        colNameV.add("Supplier Name");
        colNameV.add("Contact Info");

        // Step 2: Set the rows of the JTable

        // 2.a Retrieve all supplier data
        SupplierDAO supplierDAO = new SupplierDAO();
        java.util.List<Supplier> sList = null; // Initialize a list to store supplier data
        try {
            sList = supplierDAO.getAllSupplier(); // Call getAllSupplier from the DAO to retrieve all suppliers
        } catch (SQLException e) {
            // Handle any SQL exceptions by displaying the error message
            jLabelStatus.setText("Database Error! " + e.getMessage());
        }

        Vector rowDataVector = new Vector(); // Initialize a vector to store row data

        // Iterate through each supplier in the sList
        for (Supplier s : sList) {
            Vector<Object> cellVector = new Vector<>(); // Initialize cellVector for each supplier
            cellVector.add(s.getId()); // Add Supplier ID to cellVector
            cellVector.add(s.getName()); // Add Supplier Name to cellVector
            cellVector.add(s.getContactInfo()); // Add Contact Info to cellVector
            rowDataVector.add(cellVector); // Add cellVector to rowDataVector to create the vector row
        }      

        //Initialize the JTable instance using the row data and column data vectors
            jTableSupplier = new JTable(rowDataVector, colNameV);
            jScrollPane1.setViewportView(jTableSupplier);
    }

