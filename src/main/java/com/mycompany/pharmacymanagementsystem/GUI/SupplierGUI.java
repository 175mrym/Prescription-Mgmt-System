    private void jBtnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnUpdateActionPerformed
    
       // Read input data from text fields
    String supplierID = jTfID.getText().trim();
    String supplierName = jTfName.getText().trim();
    String supplierInfo = jTfInfo.getText().trim();

if (supplierID.isEmpty() || supplierName.isEmpty() || supplierInfo.isEmpty()) {
    // Display an error message
    jLabelStatus.setText("Please enter data");
    // Exit the program
    return;
}

SupplierDAO supplierDAO = new SupplierDAO();
// Before updating the supplier, make sure that the ID exists in the database

int updateStatus = -1;
try {
    Supplier existingSupplier = supplierDAO.findSupplierById(supplierID);
    if (existingSupplier == null) {
        jLabelStatus.setText("Supplier does not exist in the DB");
        return;
    }
    
    // Update the supplier
    updateStatus = supplierDAO.updateSupplier(supplierID, supplierName, supplierInfo);

    if (updateStatus == 1) {
        jLabelStatus.setText("Supplier " + supplierID + " is updated!");
        // Reload all suppliers
        loadAllSupplier();
    } else {
        jLabelStatus.setText("No record is updated");
    }
} catch (SQLException e) {
    jLabelStatus.setText("Database Error: " + e.getMessage());
}

        
        
    }//GEN-LAST:event_jBtnUpdateActionPerformed

